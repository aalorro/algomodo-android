class SuperformulaGenerator : Generator {

    override val id = "geo-superformula"
    override val family = "geometry"
    override val styleName = "Superformula"
    override val definition =
        "Shapes generated by the Gielis superformula, a generalization of the superellipse producing diverse natural and geometric curves."
    override val algorithmNotes =
        "Evaluates the polar superformula r = (|cos(m*theta/4)/a|^n2 + |sin(m*theta/4)/b|^n3)^(-1/n1) " +
        "at many angle samples around the full circle. Multiple concentric layers with varying parameters " +
        "and rotational copies create complex rosette-like compositions."
    override val supportsVector = false
    override val supportsAnimation = true

